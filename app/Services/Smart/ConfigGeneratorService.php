    public function suggestForDevice(Device $device): array
    {
        $hostname = $device->hostname ?? 'UNKNOWN';
        $platform = strtolower($device->platform ?? '');
        $vendor = strtolower($device->vendor ?? '');
        $mgmtIp = $device->mgmt_ip ?? '';
        $inventory = $device->inventory;

        $missingHints = [];

        if (empty($device->hostname)) {
            $missingHints[] = 'Missing hostname';
        }
        if (empty($device->mgmt_ip)) {
            $missingHints[] = 'Missing management IP';
        }
        if (empty($device->platform)) {
            $missingHints[] = 'Missing platform';
        }
        if (empty($device->auth_username)) {
            $missingHints[] = 'Missing authentication username';
        }

        $deviceProfile = 'GENERIC';
        if (str_contains($platform, 'switch') || str_contains($platform, 'catalyst')) {
            $deviceProfile = 'SWITCH';
        } elseif (str_contains($platform, 'router') || str_contains($platform, 'isr')) {
            $deviceProfile = 'ROUTER';
        } elseif (str_contains($platform, 'firewall') || str_contains($platform, 'asa')) {
            $deviceProfile = 'FIREWALL';
        }

        $routingProtocol = 'none';
        if ($deviceProfile === 'ROUTER') {
            $routingProtocol = 'ospf';
            if ($inventory && str_contains(strtolower($inventory->name ?? ''), 'enterprise')) {
                $routingProtocol = 'eigrp';
            }
        }

        $vlanId = 10;
        if ($deviceProfile === 'SWITCH') {
            $vlanId = match (true) {
                str_contains($hostname, 'access') => 20,
                str_contains($hostname, 'mgmt') => 99,
                str_contains($hostname, 'voice') => 30,
                default => 10,
            };
        }

        $ipRange = '192.168.10.0/24';
        if (! empty($mgmtIp)) {
            $parts = explode('.', $mgmtIp);
            if (count($parts) === 4) {
                $thirdOctet = (int) $parts[2];
                $thirdOctet = ($thirdOctet + 10) % 254;
                $ipRange = "192.168.{$thirdOctet}.0/24";
            }
        }

        $inventoryName = $inventory?->name ?? 'default';
        if (str_contains($inventoryName, 'datacenter')) {
            $ipRange = '10.0.10.0/24';
            if ($deviceProfile === 'ROUTER') {
                $routingProtocol = 'bgp';
            }
        }

        return [
            'device_profile' => $deviceProfile,
            'recommended_routing_protocol' => $routingProtocol,
            'suggested_vlan_id' => $vlanId,
            'suggested_ip_range' => $ipRange,
            'missing_hints' => $missingHints,
            'device_info' => [
                'hostname' => $hostname,
                'platform' => $device->platform ?? 'Unknown',
                'vendor' => $device->vendor ?? 'Unknown',
                'mgmt_ip' => $mgmtIp,
                'inventory' => $inventoryName,
                'connection' => $device->connection ?? 'network_cli',
                'status' => $device->status ?? 'unknown',
            ],
        ];
    }
}